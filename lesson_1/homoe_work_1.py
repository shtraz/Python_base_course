"""
Задача 6: Вы пользуетесь общественным транспортом? 
Вероятно, вы расплачивались за проезд и получали билет с номером. 
Счастливым билетом называют такой билет с шестизначным номером, где сумма первых трех цифр равна сумме последних трех. 
Т.е. билет с номером 385916 – счастливый, т.к. 3+8+5=9+1+6. Вам требуется написать программу, которая проверяет счастливость билета.

*Пример:*

385916 -> yes
123456 -> no
"""

ticket = input("Введите шестизначный номер билета: ")
if int(ticket[0]) + int(ticket[1]) + int(ticket[2]) == int(ticket[3]) + int(
        ticket[4]) + int(ticket[5]):
    print(f'билет № {ticket} счастливый')
else:
    print(f'билет № {ticket} НЕ счастливый')

"""
Задача 8: Требуется определить, можно ли от шоколадки размером n × m долек отломить k долек, 
если разрешается сделать один разлом по прямой между дольками (то есть разломить шоколадку на два прямоугольника).

*Пример:*

3 2 4 -> yes
3 2 1 -> no
"""
n = int(input('Введите количество n-долек: '))
m = int(input('Введите количество m-долек: '))
k = int(input('Введите количество k-долек: '))

if k < (n * m) and ((k % n == 0) or (k % m == 0)):
    print(f'От шоколадки размером {n}X{m} можно отломить {k} долек')
else:
    print(f'От шоколадки размером {n}X{m} НЕЛЬЗЯ отломить {k} долек')


