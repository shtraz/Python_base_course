"""
Урок 1. Ввод-Вывод, операторы ветвления
Задача 2: Найдите сумму цифр трехзначного числа.

*Пример:*

123 -> 6 (1 + 2 + 3)
100 -> 1 (1 + 0 + 0) |
"""

user_number = input("\nВведите целое число: ")
sum_numbers = 0

for el in user_number:
    sum_numbers += int(el)

print(f'Сумма чисел строки {user_number} = {sum_numbers}')

